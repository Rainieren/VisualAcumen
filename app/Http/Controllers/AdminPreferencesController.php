    public function saveSidebar(Request $request)
    {
        $sidebar = Sidebar::find(Auth::user()->company->sidebar->id);

        $sidebar->background_color = request('background_color');
        $sidebar->text_color = request('text_color');
        $sidebar->active_background = request('active_background');
        $sidebar->hover_background = request('hover_background');
        $sidebar->save();

        return redirect('/admin_preferences');
    }
